import java.util.*;

public class SumaNumeros {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);


        String a = sc.next();
        String b = sc.next();


        int n1 = Integer.parseInt(a);
        int n2 = Integer.parseInt(b);


        System.out.println(n1 + n2);
    }
}